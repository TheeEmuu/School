public class PrimeOrEven {
    public static void main(String[] arg){
        int number;

        System.out.print("Please input a number between 1 and 20: ");
        number = TextIO.getInt();

        switch(number){
            case 2:
                System.out.println("Number is both prime and even");
                break;
            case 4:
            case 6:
            case 8:
            case 10:
            case 12:
            case 14:
            case 16:
            case 18:
            case 20:
                System.out.println("Number is even");
                break;
            case 3:
            case 5:
            case 7:
            case 11:
            case 13:
            case 17:
                System.out.println("Number is prime");
                break;
            default:
                System.out.println("Number is neither prime nor even");
        }
    }
}
