public class LCM {
    public static void main(String [] args) {
        int a, b;
        int i;

        System.out.print("Please input your first number: ");
        a = TextIO.getInt();
        System.out.print("Please input your second number: ");
        b = TextIO.getInt();
        System.out.println();

        for (i = 1;; i++) {
            if (i % a == 0 && i % b == 0)
                break;
        }

        System.out.printf("The LCM of %d and %d is %d", a, b, i);
    }
}
