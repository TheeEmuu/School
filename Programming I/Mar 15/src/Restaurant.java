public class Restaurant {
    String name;
    String type;
    String location;

    public String getName(){
        return name;
    }
    public void setName(String input){
        name = input;
    }

    public String getType(){
        return type;
    }
    public void setType(String input){
        type = input;
    }

    public String getLocation(){
        return location;
    }
    public void setLocation(String input){
         location = input;
    }
}
